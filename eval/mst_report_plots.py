"""
Script generated by AI. 
Generate the two figures referenced by report/mst_section_updated.tex.

Run from the repository root:

    python3 eval/mst_report_plots.py \
      --persistent-summary results/summary_persistent.csv \
      --optdigits-summary results/summary_persistent_optdigits.csv \
      --out-dir report/figures

Requires pandas and matplotlib.
"""

import argparse
from pathlib import Path

import matplotlib.pyplot as plt
import pandas as pd


RUNTIME_FIGURE = "mst_persistent_runtime_vs_n"
SPEEDUP_FIGURE = "mst_persistent_speedup_vs_threads"
THREADS = [1, 2, 3, 4, 8, 12, 16]


def read_results(persistent_path, optdigits_path):
    persistent = pd.read_csv(persistent_path)
    optdigits = pd.read_csv(optdigits_path)
    combined = pd.concat([persistent, optdigits], ignore_index=True)

    required = {
        "dataset",
        "n",
        "d",
        "threads",
        "median_runtime_ms",
        "speedup_vs_1_thread",
    }
    missing = required - set(combined.columns)
    if missing:
        raise SystemExit(f"Missing columns in summary CSVs: {sorted(missing)}")

    return combined


def save_figure(fig, out_dir, stem):
    fig.savefig(out_dir / f"{stem}.pdf", bbox_inches="tight")
    fig.savefig(out_dir / f"{stem}.png", dpi=200, bbox_inches="tight")
    plt.close(fig)


def plot_runtime_vs_n(results, out_dir):
    random_2d = results[
        results["dataset"].str.startswith("random_n")
        & (results["d"] == 2)
        & results["threads"].isin(THREADS)
    ].copy()

    fig, ax = plt.subplots(figsize=(6.8, 4.8))
    for threads in THREADS:
        group = random_2d[random_2d["threads"] == threads].sort_values("n")
        ax.plot(
            group["n"],
            group["median_runtime_ms"],
            marker="o",
            linewidth=1.7,
            markersize=4.5,
            label=f"{threads} thread{'s' if threads != 1 else ''}",
        )

    ax.set_xscale("log")
    ax.set_yscale("log")
    ax.set_xlabel("Number of points $N$")
    ax.set_ylabel("Median runtime (ms)")
    ax.set_title("Persistent-thread MST: runtime scaling ($d=2$)")
    ax.grid(True, which="both", alpha=0.25)
    ax.legend(fontsize=8, ncol=2)
    fig.tight_layout()
    save_figure(fig, out_dir, RUNTIME_FIGURE)


def plot_speedup_vs_threads(results, out_dir):
    selected = [
        "random_n500",
        "random_n2000",
        "random_n5000",
        "complex9",
        "optdigits",
    ]
    labels = {
        "random_n500": r"random ($N=500,d=2$)",
        "random_n2000": r"random ($N=2000,d=2$)",
        "random_n5000": r"random ($N=5000,d=2$)",
        "complex9": r"complex9 ($N=3031,d=2$)",
        "optdigits": r"optdigits ($N=5620,d=64$)",
    }

    fig, ax = plt.subplots(figsize=(6.8, 4.8))
    for dataset in selected:
        group = results[results["dataset"] == dataset].sort_values("threads")
        ax.plot(
            group["threads"],
            group["speedup_vs_1_thread"],
            marker="o",
            linewidth=1.8,
            markersize=4.5,
            label=labels[dataset],
        )

    ax.plot(THREADS, THREADS, linestyle="--", color="black",
            linewidth=1.2, alpha=0.55, label="ideal linear")
    ax.set_xticks(THREADS)
    ax.set_xlabel("Number of threads")
    ax.set_ylabel("Speedup $T_1/T_p$")
    ax.set_title("Persistent-thread MST: speedup")
    ax.grid(True, alpha=0.25)
    ax.legend(fontsize=7.6)
    fig.tight_layout()
    save_figure(fig, out_dir, SPEEDUP_FIGURE)


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--persistent-summary", required=True)
    parser.add_argument("--optdigits-summary", required=True)
    parser.add_argument("--out-dir", default="report/figures")
    args = parser.parse_args()

    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    results = read_results(args.persistent_summary, args.optdigits_summary)
    plot_runtime_vs_n(results, out_dir)
    plot_speedup_vs_threads(results, out_dir)

    print(f"Wrote {out_dir / (RUNTIME_FIGURE + '.pdf')}")
    print(f"Wrote {out_dir / (SPEEDUP_FIGURE + '.pdf')}")


if __name__ == "__main__":
    main()
