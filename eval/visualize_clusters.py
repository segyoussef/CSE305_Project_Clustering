"""
Credits: script generated by Claude. 
Visualize the single-link clustering of any 2D labelled dataset produced by hc_mst_cpu.

Usage:
    python3 eval/visualize_clusters.py <dataset_name> <k>

Example:
    python3 eval/visualize_clusters.py donut3 3
    python3 eval/visualize_clusters.py complex9 9
"""

import argparse
import subprocess
from io import StringIO
from pathlib import Path

import matplotlib.pyplot as plt
import numpy as np
import pandas as pd
from scipy.cluster.hierarchy import fcluster

REPO_ROOT = Path(__file__).resolve().parent.parent


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("dataset", help="dataset name (without .csv)")
    parser.add_argument("k", type=int, help="number of clusters to cut at")
    args = parser.parse_args()

    csv_path = REPO_ROOT / f"eval/datasets/{args.dataset}.csv"
    df = pd.read_csv(csv_path)
    has_labels = "label" in df.columns
    coord_cols = [c for c in df.columns if c != "label"]
    if len(coord_cols) != 2:
        raise SystemExit(f"Only 2D datasets can be visualized; got {len(coord_cols)}D.")
    points = df[coord_cols].values

    exe = REPO_ROOT / "build/hc_mst_cpu"
    cmd = [str(exe), str(csv_path), "--threads", "1"]
    if has_labels:
        cmd.append("--has-labels")
    result = subprocess.run(cmd, capture_output=True, text=True, check=True)
    dendro = pd.read_csv(StringIO(result.stdout))

    Z = dendro[["cluster_a", "cluster_b", "distance", "new_size"]].values.astype(float)
    labels = fcluster(Z, t=args.k, criterion="maxclust")

    fig, ax = plt.subplots(figsize=(6, 6))
    for c in np.unique(labels):
        mask = labels == c
        ax.scatter(points[mask, 0], points[mask, 1], s=8, label=f"Cluster {c}")
    ax.set_aspect("equal")
    ax.set_title(f"{args.dataset}: {args.k} clusters recovered by MST-based single-link clustering")
    ax.legend(loc="best", fontsize=8, ncol=2)
    ax.grid(True, alpha=0.3)

    out_path = REPO_ROOT / f"results/plots/{args.dataset}_clustering.pdf"
    out_path.parent.mkdir(parents=True, exist_ok=True)
    fig.tight_layout()
    fig.savefig(out_path, dpi=150)
    plt.close(fig)
    print(f"Wrote {out_path}")


if __name__ == "__main__":
    main()