"""
Credits: Script generated by Claude.
Generate runtime, speedup, and CUDA block-size plots from results/runtimes.csv.

Usage from the repository root:
    python3 eval/plots.py

Produces PNG files and a tidied summary CSV in results/plots/.
"""

import argparse
from pathlib import Path


REPO_ROOT = Path(__file__).resolve().parent.parent
IN_CSV = REPO_ROOT / "results" / "runtimes.csv"
OUT_DIR = REPO_ROOT / "results" / "plots"

pd = None
plt = None


def load_plot_dependencies():
    global pd, plt
    try:
        import matplotlib.pyplot as matplotlib_pyplot
        import pandas as pandas
    except ModuleNotFoundError as exc:
        raise SystemExit(
            "Missing Python plotting dependency. Install pandas and matplotlib, "
            "then rerun eval/plots.py. Original error: " + str(exc)
        )
    pd = pandas
    plt = matplotlib_pyplot


def median_per_config(df):
    """Reduce repetitions by taking the median runtime per benchmark config."""
    group_cols = [
        "executable",
        "implementation_type",
        "dataset",
        "n",
        "d",
        "dataset_source",
        "linkage",
        "threads",
        "block_size",
    ]
    return (df.dropna(subset=["runtime_ms"])
              .groupby(group_cols, as_index=False, dropna=False)
              ["runtime_ms"].median())


def compute_speedup(df):
    """Add CPU speedup = T(1 thread) / T(p threads)."""
    cpu = df[df["implementation_type"] == "cpu"].copy()
    non_cpu = df[df["implementation_type"] != "cpu"].copy()

    base = (cpu[cpu["threads"] == 1]
            .rename(columns={"runtime_ms": "runtime_1"})
            [["executable", "dataset", "linkage", "runtime_1"]])
    cpu = cpu.merge(base, on=["executable", "dataset", "linkage"], how="left")
    cpu["speedup"] = cpu["runtime_1"] / cpu["runtime_ms"]
    non_cpu["runtime_1"] = pd.NA
    non_cpu["speedup"] = pd.NA
    return pd.concat([cpu, non_cpu], ignore_index=True)


def dataset_subset(df):
    datasets_to_show = [
        "random_n500",
        "random_n1000",
        "random_n2000",
        "random_n5000",
        "donut3",
        "complex9",
        "diamond9",
    ]
    return df[df["dataset"].isin(datasets_to_show)].copy()


def plot_runtime_vs_n(df, executable, out_path):
    sub = df[(df["executable"] == executable)
             & (df["dataset"].str.startswith("random_n"))].copy()
    if sub.empty:
        return

    sub = sub.sort_values(["threads", "block_size", "n"])
    fig, ax = plt.subplots(figsize=(7, 5))

    if sub["implementation_type"].iloc[0] == "cuda":
        for block_size, grp in sub.groupby("block_size"):
            ax.plot(grp["n"], grp["runtime_ms"],
                    marker="o", label=f"block {int(block_size)}")
    else:
        for threads, grp in sub.groupby("threads"):
            ax.plot(grp["n"], grp["runtime_ms"],
                    marker="o", label=f"{int(threads)} thread(s)")

    ax.set_xscale("log")
    ax.set_yscale("log")
    ax.set_xlabel("N (number of points)")
    ax.set_ylabel("Runtime (ms)")
    ax.set_title(f"{executable}: runtime vs N")
    ax.legend(fontsize=8)
    ax.grid(True, which="both", alpha=0.3)

    fig.tight_layout()
    fig.savefig(out_path, dpi=120)
    plt.close(fig)
    print(f"  Wrote {out_path}")


def plot_speedup_vs_threads(df, executable, out_path):
    sub = df[(df["executable"] == executable)
             & (df["implementation_type"] == "cpu")].copy()
    sub = dataset_subset(sub).sort_values(["dataset", "threads"])
    if sub.empty:
        return

    fig, ax = plt.subplots(figsize=(7, 5))
    for dataset, grp in sub.groupby("dataset"):
        ax.plot(grp["threads"], grp["speedup"],
                marker="o", label=f"{dataset} (N={int(grp['n'].iloc[0])})")

    max_t = int(sub["threads"].max())
    ax.plot([1, max_t], [1, max_t], "k--", alpha=0.3, label="ideal")

    ax.set_xlabel("Number of threads")
    ax.set_ylabel("Speedup (T1 / Tp)")
    ax.set_title(f"{executable}: speedup vs thread count")
    ax.legend(fontsize=8)
    ax.grid(True, alpha=0.3)

    fig.tight_layout()
    fig.savefig(out_path, dpi=120)
    plt.close(fig)
    print(f"  Wrote {out_path}")


def plot_runtime_bars(df, executable, out_path):
    sub = dataset_subset(df[df["executable"] == executable]).copy()
    if sub.empty:
        return

    if sub["implementation_type"].iloc[0] == "cuda":
        columns = "block_size"
        legend = "Block size"
    else:
        columns = "threads"
        legend = "Threads"

    pivot = sub.pivot_table(index="dataset", columns=columns,
                            values="runtime_ms", aggfunc="median")
    n_map = dict(zip(sub["dataset"], sub["n"]))
    pivot = pivot.loc[sorted(pivot.index, key=lambda d: n_map[d])]

    fig, ax = plt.subplots(figsize=(11, 5))
    pivot.plot(kind="bar", ax=ax, logy=True, width=0.75)
    ax.set_xlabel("Dataset")
    ax.set_ylabel("Runtime (ms, log scale)")
    ax.set_title(f"{executable}: runtime per dataset")
    ax.legend(title=legend, fontsize=9)
    ax.grid(True, which="major", axis="y", alpha=0.3)
    plt.xticks(rotation=45, ha="right")

    fig.tight_layout()
    fig.savefig(out_path, dpi=120)
    plt.close(fig)
    print(f"  Wrote {out_path}")


def plot_cuda_block_sizes(df, executable, out_path):
    sub = dataset_subset(df[(df["executable"] == executable)
                            & (df["implementation_type"] == "cuda")]).copy()
    if sub.empty:
        return

    sub = sub.sort_values(["dataset", "block_size"])
    fig, ax = plt.subplots(figsize=(7, 5))
    for dataset, grp in sub.groupby("dataset"):
        ax.plot(grp["block_size"], grp["runtime_ms"],
                marker="o", label=f"{dataset} (N={int(grp['n'].iloc[0])})")

    ax.set_xlabel("CUDA block size")
    ax.set_ylabel("Runtime (ms)")
    ax.set_title(f"{executable}: runtime vs CUDA block size")
    ax.legend(fontsize=8)
    ax.grid(True, alpha=0.3)

    fig.tight_layout()
    fig.savefig(out_path, dpi=120)
    plt.close(fig)
    print(f"  Wrote {out_path}")


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--in-csv", default=str(IN_CSV))
    parser.add_argument("--out-dir", default=str(OUT_DIR))
    args = parser.parse_args()
    load_plot_dependencies()

    in_path = Path(args.in_csv)
    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    df = pd.read_csv(in_path)
    print(f"Loaded {len(df)} rows from {in_path}")

    df = median_per_config(df)
    df = compute_speedup(df)

    for executable in sorted(df["executable"].unique()):
        print(f"\nPlots for {executable}:")
        plot_runtime_vs_n(df, executable,
                          out_dir / f"{executable}_runtime_vs_n.png")
        plot_speedup_vs_threads(df, executable,
                                out_dir / f"{executable}_speedup_vs_threads.png")
        plot_runtime_bars(df, executable,
                          out_dir / f"{executable}_runtime_bars.png")
        plot_cuda_block_sizes(df, executable,
                              out_dir / f"{executable}_cuda_block_sizes.png")

    summary_path = out_dir / "summary.csv"
    df.to_csv(summary_path, index=False)
    print(f"\nWrote summary table: {summary_path}")


if __name__ == "__main__":
    main()
