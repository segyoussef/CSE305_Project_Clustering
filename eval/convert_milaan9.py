"""
Credits: Script generated by Claude.
Convert milaan9 CSV files to the team's standard format.

Reads from a source directory and writes to eval/datasets/ with:
  - Coordinate columns renamed to x, y (or x0, x1, ... for >2D)
  - Label column renamed to `label` and integer-encoded 0..k-1

Run from repo root:
    python3 eval/convert_milaan9.py
"""

import pandas as pd
from pathlib import Path

SRC = Path("eval/datasets/milaan9_raw")   # put the downloaded files here
OUT = Path("eval/datasets")

FILES = ["donut3.csv", "complex9.csv", "diamond9.csv"]


def convert(src_path: Path, out_path: Path):
    df = pd.read_csv(src_path)

    # The label column is typically the last column. milaan9 uses 'class',
    # we standardize to 'label'.
    label_col = df.columns[-1]
    coord_cols = list(df.columns[:-1])

    out = df[coord_cols].copy()
    if len(coord_cols) == 2:
        out.columns = ["x", "y"]
    else:
        out.columns = [f"x{i}" for i in range(len(coord_cols))]

    out["label"] = pd.Categorical(df[label_col]).codes

    out.to_csv(out_path, index=False)
    print(f"  Wrote {out_path}  (N={len(out)}, d={len(coord_cols)})")


for fname in FILES:
    src = SRC / fname
    out = OUT / fname
    if not src.exists():
        print(f"  SKIP: {src} not found")
        continue
    convert(src, out)

print("\nDone.")